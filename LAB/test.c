#include <stdio.h>

int main(void)
{
    int i = 0;
    char c = 42;
    char mask = 1;
    while (i < 8)
    {
        mask = mask & c;
        printf("mask = %d\n", (int)mask);
        mask = 1;
        i++;
        mask = mask << 1;
    }
}